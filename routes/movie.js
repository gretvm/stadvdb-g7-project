class Movie {
    constructor(id, title, year, rating, genre, first_name, last_name){
        this.id = id;
        this.title = title;
        this.year = year;
        this.rating = rating;
        this.genre = genre;
        this.first_name = first_name;
        this.last_name = last_name;
    }
}

module.exports = Movie;